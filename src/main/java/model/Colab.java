package model;

public class Colab {
	private Integer idColab;
	private String nomeColab;
	private String linkColab;
	private String imgColab;
	private String tamimgColab;
	
	public Integer getIdColab() {
		return idColab;
	}
	public void setIdColab(Integer idColab) {
		this.idColab = idColab;
	}
	public String getNomeColab() {
		return nomeColab;
	}
	public void setNomeColab(String nomeColab) {
		this.nomeColab = nomeColab;
	}
	public String getLinkColab() {
		return linkColab;
	}
	public void setLinkColab(String linkColab) {
		this.linkColab = linkColab;
	}
	public String getImgColab() {
		return imgColab;
	}
	public void setImgColab(String imgColab) {
		this.imgColab = imgColab;
	}
	public String getTamimgColab() {
		return tamimgColab;
	}
	public void setTamimgColab(String tamimgColab) {
		this.tamimgColab = tamimgColab;
	}
}
